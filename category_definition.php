    <td valign="top" style="color:#666; background-color:#59ACFF"><strong>7. Product Issues (JIATF)</strong></td>
    <td valign="top" style="color:#666; background-color:#BFDFFF"><strong>Sub-category</strong></td>
    <td valign="top">&nbsp;</td>
  </tr>
  <tr>
    <td rowspan="5" valign="top" style="color:#666; background-color:#59ACFF">In the context of JIATF work, diversion almost exclusively relates to Global Fund AMFm ACTs sold to private sector 1st Line Buyers (1LB). 1LB are not permitted to on-sell AMFm ACTs to other wholesalers/outlets outside of the country.  Diversion could also relate to other products, if the products in question were intended only for specified countries’ private commercial sectors, and were then found outside of the countries for which they were intended.</td>
    <td valign="top" style="color:#666; background-color:#BFDFFF">a)   Diversion</td>
    <td valign="top"><strong>M</strong>ovement of donor-funded products which are intended to be available for the private commercial sector in only one specified country, into the  private commercial sector of a different country. </td>
  </tr>
  <tr>
    <td valign="top" style="color:#666; background-color:#BFDFFF">b)   Counterfeit</td>
    <td valign="top"><strong>P</strong>roducts which contain little to no active pharmaceutical ingredient, and which falsely bear the image and packaging of a WHO pre-qualified medicine.</td>
  </tr>
  <tr>
    <td valign="top" style="color:#666; background-color:#BFDFFF">c)   Theft</td>
    <td valign="top"><strong>M</strong>ovement of donor-funded products which are intended to be available only in the public sector supply chain (public hospitals, clinics, etc.) available free to the end user, to the private commercial sector and available for sale. JIATF distinguishes between “cross-border theft”, where a product was intended for the public sector in one country and is found in the private sector of a different country, and “in-country theft”, where a product was intended for the public sector of one country and is available in the private sector of the same country.</td>
  </tr>
  <tr>
    <td valign="top" style="color:#666; background-color:#BFDFFF">d)   Faulty Product</td>
    <td valign="top"><strong>A</strong> product which does not contain the requisite amount of API.  This can be a genuine product, produced by a WHO prequalified supplier, which is substandard in relation to the WHO requirements.  It can also be a non-WHO prequalified medicine which does not contain amounts of API sufficient to treat malaria.  Note: JIATF does not focus on products acquired which are not WHO prequalified. </td>
  </tr>
  <tr>
    <td valign="top" style="color:#666; background-color:#BFDFFF">e)   Expired Drugs</td>
    <td valign="top"><strong>A</strong> product which is in circulation past the expiry date indicated by the manufacturer on the packaging.  A product can be expired and faulty (both past the expiry data and lacking requisite level of API), or could be expired and non-faulty (past the expiry date but containing requisite level of API). </td>
